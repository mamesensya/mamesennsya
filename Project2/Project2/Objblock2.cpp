//アクション
void CObjBlock::Action()
{
	//	主人公の位置を取得
	CObjHero* hero = (CObjHero*)Objs::GetObj(OBJ_HERO);
	float hx = hero->GetX();
	float hy = hero->GetY();

	//右方スクロールライン
	if (hx < 80)
	{
		hero->SetX(80);
		m_scroll -= hero->GetVX();
	}

	//左方スクロールライン
	if (hx > 300)
	{
		hero->SetX(300);
		m_scroll -= hero->GetVX();
	}
	//上方スクロールライン
	if (hy < 80)
	{
		hero->SetY(80);
		m_scroll2 -= hero->GetVY();
	}

	//下方スクロールライン
	if (hy > 300)
	{
		hero->SetY(300);
		m_scroll2 -= hero->GetVY();
	}

	
}

