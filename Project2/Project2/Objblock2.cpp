//BlockHit関数
//引数1 float* x        :判定を行うobjectのX位置
//引数2 float* y        :判定を行うobjectのY位置
//引数3 bool* up        :上下左右判定の上部分に当たっているかどうかを返す
//引数4 bool* down      :下部分に当たっているかを返す
//引数5 bool* left      :左部分に当たっているかを返す
//引数6 bool* right     :右部分に当たっているかを返す
//引数7 float* vx       :左右判定時の反発による移動方向・力の値を変えて返す
//引数8 float* vy       :上下判定時による自由落下運動の移動方向・力の値を変えて返す
void BlockHit(
	float* x, float* y, bool* up, bool* down,
	bool* left, bool* right, float* vx, float* vy
)
{

	*up = false;
	*down = false;
	*left = false;
	*right = false;


	for (int i = 0; i < 60; i++)
	{
		for (int j = 0; j < 80; j++)
		{
			if (m_map[i][j] == 1)
			{
				//要素番号を座標に変換
				float x = j * 64.0f;
				float y = i * 64.0f;

				//主人公とブロックのあたり判定
				if ((hx + (-m_scroll) + 64.0f > x) && (hx + (-m_scroll) < x + 64.0f) && (hy + (-m_scroll2) + 64.0f > y) && (hy + (-m_scroll2) < y + 64.0f))
				{
					//上下左右の判定

					//vectorのさくせい
					float vx = (hx + (-m_scroll)) - x;
					float vy = (hy + (-m_scroll2)) - y;

					//長さを求める
					float len = sqrt(vx * vx + vy * vy);

					float r = atan2(vy, vx);
					r = r * 180.0f / 3.14f;


					if (r < 0.0f)
						r = abs(r);
					else
						r = 360.0f - abs(r);

					if (len < 88.0f)
					{


						//角度で上下左右を判定
						if ((r < 45 && r>0) || r > 315)
						{
							//右
							hero->SetLeft(true);
							hero->SetX(x + 64.0f + (m_scroll));
							hero->SetVX(-hero->GetVX() * 0.1f);


						}
						if (r > 45 && r < 135)
						{
							//上
							hero->SetDown(true);
							hero->SetY(y - 64.0f + (m_scroll2));
							hero->SetVY(-hero->GetVY() * 0.1f);




						}
						if (r > 135 && r < 225)
						{
							//左
							hero->SetRight(true);
							hero->SetX(x - 64.0f + (m_scroll));
							hero->SetVX(-hero->GetVX() * 0.1f);




						}
						if (r > 225 && r < 315)
						{
							//下
							hero->SetUp(true);
							hero->SetY(y + 64.0f + (m_scroll2));
							hero->SetVY(-hero->GetVY() * 0.1f);


						}

					}

				}




			}



		}
		wchar_t str[128];
		float c[4] = { 1.0f,1.0f,1.0f,1.0f };
		swprintf_s(str, L"<Hero Position> X=%f , Y=%f", hero->GetX(), hero->GetY());
		Font::StrDraw(str, 10, 10, 20, c);
	}

}
